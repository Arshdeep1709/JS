//                   Arithmetic operators

// We can perform Arithmetic operations on literals, variables and expressions


// The (+ : Plus) Operator:

// let a=8;
// let b=8;
// console.log(a+b)

// The (- : Subtraction) Operator:

// let a = 3-9;
// console.log(a)

// The (* : Multiplication) Operator:

// let a=5;
// let b=(4*3)*a
// console.log(b)

// The (/ : Division) Operator:
// Division operator always returns with quotent (upper part of division)

// let a=2;
// let b=12;
// console.log(b/a)

// The (% : Modulus) Operator:
// Modulus operator always returns with remainder (lower part of division)

// let a=9;
// let b=3;
// console.log(a%b)

// The (++ : Increment) Operator:
// Increases value by 1

// let a=4;
// a++
// console.log(a)

// The (-- : Decrement) Operator:
// Decreases value by 1

// let a=5;
// a--
// console.log(a)

// The (** : Exponentiation) Operator:
// it gives you the power of the  operand 

// let a=3;
// console.log(a**3)


//      Operator precedence

// It describes, in which order will arithmetic operation performs:
//  DMAS

// let a=9+3*7;
// console.log(a);

// We can also change the order by adding brackets:

// let a=(9+3)*7
// console.log(a);




